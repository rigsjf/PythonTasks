# Percorrer listas

numeros = [10, 20, 30, 40, 50, 60]
#           0   1   2   3   4   5

# Loop com indices
for indice in range(len(numeros)):
    print(numeros[indice])

# Loop diretamente com os elementos
for numero in numeros:
    print(numero)

nomes = ["paulo", "matheus"]
for nome in nomes:
    print(nome)