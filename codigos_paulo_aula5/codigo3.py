# Funções de listas

# sum, len, max, min, sorted

numeros = [5, 4, 9, 1]

# sum -> somar todos os elementos da lista
print(sum(numeros))

# len -> ver o comprimento da lista
print(len(numeros))

# max e min -> pegam o maximo e o minimo da lista
print(max(numeros))
print(min(numeros))

# sorted -> ordena a lista

print(sorted(numeros)) # Ordena em crescente
print(sorted(numeros)[::-1]) # Ordena em decrescente

# Ordenar strings
letras = ['b', 'c', 'a']
print(sorted(letras))

