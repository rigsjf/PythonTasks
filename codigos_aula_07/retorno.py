# exemplo de função com retorno

# vamos fazer uma função "soma" que recebe dois números como argumentos
def soma(x, y):
    soma = x + y
    return soma

# vamos fazer o mesmo para as operações de subtração, multiplicação e divisão!
def subtrai(x, y):
    subtracao = x - y
    return subtracao

def multiplica(x, y):
    multiplicacao = x * y
    return multiplicacao

def divide(x, y):
    divisao = x / y
    return divisao
